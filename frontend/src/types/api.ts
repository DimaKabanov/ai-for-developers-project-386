/**
 * Тип события, созданный владельцем календаря.
 * Определяет категорию встречи с её названием, описанием и длительностью.
 */
export interface EventType {
  /** Уникальный идентификатор типа события */
  id: string;
  /** Название типа события, отображаемое гостям */
  name: string;
  /** Описание типа события */
  description: string;
  /** Длительность встречи в минутах */
  durationMinutes: number;
}

/**
 * Публичное представление типа события для гостей.
 * Содержит только данные, необходимые для выбора встречи.
 */
export interface EventTypeSummary {
  /** Уникальный идентификатор типа события */
  id: string;
  /** Название типа события */
  name: string;
  /** Описание типа события */
  description: string;
  /** Длительность встречи в минутах */
  durationMinutes: number;
}

/**
 * Владелец календаря. Единственный предзаданный профиль в системе.
 * Регистрация и авторизация отсутствуют — профиль используется по умолчанию
 * в административной части приложения.
 */
export interface Owner {
  /** Уникальный идентификатор владельца */
  id: string;
  /** Отображаемое имя владельца */
  name: string;
}

/**
 * Статус бронирования
 */
export type BookingStatus = 'confirmed' | 'cancelled';

/**
 * Бронирование слота гостем.
 * На одно и то же время не может существовать двух бронирований,
 * даже если они относятся к разным типам событий.
 */
export interface Booking {
  /** Уникальный идентификатор бронирования */
  id: string;
  /** Идентификатор типа события */
  eventTypeId: string;
  /** Название типа события (денормализовано для удобства отображения) */
  eventTypeName: string;
  /** Дата и время начала встречи (ISO 8601) */
  startTime: string;
  /** Дата и время окончания встречи (ISO 8601) */
  endTime: string;
  /** Имя гостя */
  guestName: string;
  /** Email гостя */
  guestEmail: string;
  /** Примечание гостя (опционально) */
  guestNote?: string;
  /** Статус бронирования */
  status: BookingStatus;
  /** Дата и время создания бронирования (ISO 8601) */
  createdAt: string;
}

/**
 * Временной слот для записи.
 * Слоты формируются на 14 дней начиная с текущей даты.
 * Слот считается занятым, если на него создано хотя бы одно бронирование
 * вне зависимости от типа события.
 */
export interface Slot {
  /** Уникальный идентификатор слота */
  id: string;
  /** Дата и время начала слота (ISO 8601) */
  startTime: string;
  /**
   * Дата и время окончания слота (ISO 8601).
   * Вычисляется как startTime + durationMinutes выбранного типа события.
   */
  endTime: string;
  /**
   * Доступен ли слот для бронирования.
   * false — если на это время уже есть хотя бы одно бронирование
   * (даже для другого типа события).
   */
  isAvailable: boolean;
}

/**
 * Тело запроса для создания бронирования гостем
 */
export interface CreateBookingRequest {
  /**
   * Дата и время начала желаемого слота (ISO 8601).
   * Должна входить в окно ближайших 14 дней от текущей даты.
   * Если слот уже занят, сервер вернёт 409 Conflict.
   */
  startTime: string;
  /** Имя гостя (обязательно) */
  guestName: string;
  /** Email гостя (обязательно) */
  guestEmail: string;
  /** Примечание к встрече (опционально) */
  guestNote?: string;
}
